package com.pakt.pojos;

public class dealerdash {
	String totaldevice;
	String damageddevice;
	String solddevice;
	String availabledevice;
	String customers;
	String dealers;
	
	
	public String getTotaldevice() {
		return totaldevice;
	}
	public void setTotaldevice(String totaldevice) {
		this.totaldevice = totaldevice;
	}
	public String getDamageddevice() {
		return damageddevice;
	}
	public void setDamageddevice(String damageddevice) {
		this.damageddevice = damageddevice;
	}
	public String getSolddevice() {
		return solddevice;
	}
	public void setSolddevice(String solddevice) {
		this.solddevice = solddevice;
	}
	public String getAvailabledevice() {
		return availabledevice;
	}
	public void setAvailabledevice(String availabledevice) {
		this.availabledevice = availabledevice;
	}
	public String getCustomers() {
		return customers;
	}
	public void setCustomers(String customers) {
		this.customers = customers;
	}
	public String getDealers() {
		return dealers;
	}
	public void setDealers(String dealers) {
		this.dealers = dealers;
	}
	
	
	
}
