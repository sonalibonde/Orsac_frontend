package com.pakt.pojos;

public class TotalDevice {

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTotal_devices() {
		return total_devices;
	}
	public void setTotal_devices(String total_devices) {
		this.total_devices = total_devices;
	}
	public String getAvailable() {
		return available;
	}
	public void setAvailable(String available) {
		this.available = available;
	}
	public String getDamaged() {
		return damaged;
	}
	public void setDamaged(String damaged) {
		this.damaged = damaged;
	}
	public String getSaled() {
		return saled;
	}
	public void setSaled(String saled) {
		this.saled = saled;
	}
	public String getDatetimestamp() {
		return datetimestamp;
	}
	public void setDatetimestamp(String datetimestamp) {
		this.datetimestamp = datetimestamp;
	}
	private String id;
	private String total_devices;
	private String available;
	private String damaged;
	private String saled;
	private String datetimestamp;
	
	
}
